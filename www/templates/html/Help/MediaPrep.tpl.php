                <h3 class="wdn-brand">File Types</h3>
                <p>At this time, to ensure your media can be played on as many devices as possible, MediaHub only supports <strong>.mp4</strong> for video files and <strong>.mp3</strong> for audio. </p>
            
                <h3 class="wdn-brand">Media Quality Specs</h3>
                <p>While there are no specific restrictions on the quality of the media added to MediaHub, we do have some recommendations to ensure your viewers have the best experience possible.</p>
                <h4 class="wdn-brand"><span class="wdn-subhead">Video</span></h4>
                <p>An aspect ratio of 16:9 is recommended on all videos regardless of resolution.  A 960px wide resolution with a bitrate of 1200kbps including audio is the preferred minimum for any videos submitted to MediaHub. Higher resolutions are encouraged.</p>
                <h4 class="wdn-brand"><span class="wdn-subhead">Audio</span></h4>
                <p>The best bitrate for .mp3 files depends on the complexity of the sounds being reproduced. A bitrate of 192kbps is typically sufficient for most applications, however, it should be noted that complex sounds files like that of an orchestra might need more while something like a single human voice can make due with much less. </p>
            
